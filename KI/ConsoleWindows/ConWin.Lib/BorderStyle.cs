namespace ConWin.Lib;

/// <summary>
/// Defines the border style for a window.
/// </summary>
public enum BorderStyle
{
    /// <summary>
    /// No border.
    /// </summary>
    None = 0,
    
    /// <summary>
    /// Single-line border.
    /// </summary>
    Single = 1,
    
    /// <summary>
    /// Double-line border.
    /// </summary>
    Double = 2
} 